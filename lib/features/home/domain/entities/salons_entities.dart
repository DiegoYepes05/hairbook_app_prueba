class Salon {
  final String id;
  final String name;
  final String location;
  final String description;
  final int creditCost;
  final String image;
  final String type;

  Salon({
    required this.id,
    required this.name,
    required this.location,
    required this.description,
    required this.creditCost,
    required this.image,
    required this.type,
  });
}
